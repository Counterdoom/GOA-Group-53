# მაგალითები (+)
x = 10 + 5
y = 20 + 10
z = 50 + 25
w = 7 + 3

print(x)  # 15
print(y)  # 30
print(z)  # 75
print(w)  # 10

# მაგალითები (-)
x = 10 - 5
y = 20 - 10
z = 50 - 25
w = 7 - 3

print(x)  # 5
print(y)  # 10
print(z)  # 25
print(w)  # 4

# მაგალითები (*)
x = 10 * 5
y = 20 * 10
z = 50 * 2
w = 7 * 6

print(x)  # 50
print(y)  # 200
print(z)  # 100
print(w)  # 42

